#include "./includes/libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	if (dst < src)
		return (ft_memcpy(dst, src, len));
	if (src < dst)
		return (ft_memcpy_bwd(dst, src, len));
	return (dst);
}
